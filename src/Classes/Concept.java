package Classes;

/**
 *
 * @author DovanuSala.lt
 */
public class Concept {
    private final String label;
    private final String value;

    public Concept(String label, String value) {
        this.label = label;
        this.value = value;
    }

    public String getLabel() {
        return label;
    }

    public String getValue() {
        return value;
    }
}
