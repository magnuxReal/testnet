    private void AllSeries(){

        try {
            Statement st = mysqlConnect.connect().createStatement();
            ResultSet res = st.executeQuery("SELECT * FROM  dm_invoice_serie");
            
          
            while (res.next()) {
        
                int last_number = res.getInt("last_number");
                int nr = res.getInt("number");
                String serie = res.getString("serie");
                String numberAsString = String.format("%0"+nr+"d%s",0 , last_number);

                jInvoiceSeries.addItem(serie+' '+numberAsString);
            }
           
            
        } catch (SQLException e) {
        } finally {
            //mysqlConnect.disconnect();
             
        }
    }
    
