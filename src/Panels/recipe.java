    private void jButton1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MousePressed
  
        JPanel newPanel = new JPanel(new GridBagLayout());
   
        
        newPanel.setBackground(new Color(255,255,255));
        GridBagConstraints constraints = new GridBagConstraints();
        save_recipe.setBackground(new JButton().getBackground());
        constraints.anchor = GridBagConstraints.WEST;
        constraints.insets = new Insets(1, 1, 1, 1);
  
        // add components to the panel
        constraints.gridx = 0;
        constraints.gridy = 0;     
        newPanel.add(recipe_n, constraints);
    
        constraints.gridx = 1;
        newPanel.add(recipe_name, constraints);
         
        constraints.gridx = 0;
        constraints.gridy = 1;     
        newPanel.add(recipe_no, constraints);
         
        constraints.gridx = 1;
        newPanel.add(recipe_note, constraints);
         
        constraints.gridx = 1;
        constraints.gridy = 2;
        constraints.anchor = GridBagConstraints.WEST;
        constraints.fill = GridBagConstraints.BOTH;
        newPanel.add(save_recipe, constraints);
   
        save_recipe.addMouseListener(new java.awt.event.MouseAdapter() {
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                String name = recipe_name.getText();
                String note = recipe_note.getText();
                
                if(name != null && !name.trim().isEmpty()){

                try {
                    Statement st = MysqlConnect.connect().createStatement(); 

                    st.executeUpdate("INSERT INTO dm_recipe (name, note) VALUES ('"+name+"', '"+note+"')");

                } catch (SQLException e) {
                } finally {
                   // mysqlConnect.disconnect();
                    jPanelSide.removeAll();
                    jPanelSide.revalidate();
                    jPanelSide.repaint();
                    recipe_name.setText("");
                    recipe_note.setText("");
                    refreshTable();
                }
                }
              
            }
        });
        
        newPanel.setBorder(new CompoundBorder(new EmptyBorder(10, 10, 10, 10), BorderFactory.createTitledBorder("Naujas receptas")));
        newPanel.setSize(440, 190);
       
        addNewPanel(newPanel);
         
    }//GEN-LAST:event_jButton1MousePressed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        jButtonRecipeDelete.setVisible(true);
        
        int index  = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        selected_id = (int) model.getValueAt(index, 0);
        selected_name = (String) model.getValueAt(index, 1);
        selected_note = (String) model.getValueAt(index, 2);
        String ouput_proc = (String) model.getValueAt(index, 3);
        
        addNewPanel(new recipeProducts(selected_id, selected_name, selected_note, ouput_proc));
  
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButtonRecipeDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRecipeDeleteActionPerformed
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Ar tikrai norite ištrinti "+selected_name+" ("+selected_note+") ?", selected_name, dialogButton);
        if(dialogResult == 0) {
            jButtonRecipeDelete.setVisible(false);
            try {
                Statement st = MysqlConnect.connect().createStatement();   
                    st.executeUpdate("UPDATE dm_recipe SET disp='0' WHERE id='"+selected_id+"' ");

            } catch (SQLException e) {
            } finally {
                
                jPanelSide.removeAll();
                jPanelSide.revalidate();
                jPanelSide.repaint();
                
                refreshTable();   
            } 
            
           
        } 
    }//GEN-LAST:event_jButtonRecipeDeleteActionPerformed

  
    
